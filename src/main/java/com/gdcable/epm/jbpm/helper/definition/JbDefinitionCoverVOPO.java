package com.gdcable.epm.jbpm.helper.definition;

import com.gdcable.epm.jbpm.po.definition.JbDefinition;
import com.gdcable.epm.jbpm.vo.definition.JbDefinitionVo;

public class JbDefinitionCoverVOPO {
	
	public static JbDefinitionVo coverPoToVo(JbDefinition po){
		JbDefinitionVo vo = new JbDefinitionVo();
		vo.setId(po.getId());
		vo.setKey(po.getKey());
		vo.setName(po.getName());
		return vo;
	}

}
