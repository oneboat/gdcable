package com.gdcable.epm.jbpm.vo;

public class PageForm {
	
	private int page;
	private int rows;
	private Class target;
	
	public PageForm(Class target){
		this.target = target;
	}
	
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public int getRows() {
		return rows;
	}
	public void setRows(int rows) {
		this.rows = rows;
	}
	public Class getTarget() {
		return target;
	}
	public void setTarget(Class target) {
		this.target = target;
	}

	

}
